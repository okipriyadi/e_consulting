        <h2>
          WBS
        </h2>
<ol>
<li>Apa WBS (Whistle Blower System) itu? <br>-	Berdasarkan Permenpan Nomor 2 Tahun 2013 tentang Pedoman Umum Sistem Penanganan Pengaduan (Whistle Blower System) Tindak Pidana Korupsi di Lingkungan Kementerian PANRB. WBS adalah mekanisme penyampaian pengaduan dugaan tindakan pidana korupsi yang telah terjadi, sedang terjadi, atau akan terjadi yang melibatkan pegawai dan orang lain yang berkaitan dengan dugaan tindak pidana korupsi yang dilakukan di Kementerian PANRB. (di menu download)
</li>
<li>Apa saja yang dapat dilaporkan dalam WBS? <br>-	Segala tindakan yang menurut undang-undang korupsi mengandung indikasi unsur tindak pidana korupsi yang terjadi di lingkungan Kementerian PANRB.
</li>
<li>Bagaimana cara pelaporan WBS? <br>-	Dapat dilaporkan pada http://wbs.menpan.go.id/</li>
</ol>
      <h2>
          Konsinyering
        </h2>
<ol>
<li>Bagaimana tata cara pengajuan izin konsinyering? <br>-	Dapat dilihat dari Nota Dinas Nomor: 14/PI.07.00/2019 (di menu download)</li>
<li>Kapan batas waktu pengajuan izin konsinyering? <br>-	Batas waktu pengajuan izin konsinyering paling lambat 3 (tiga) hari kerja sebelum dilaksanakan kegiatan
</li>
<li>Dokumen apa saja yang dibutuhkan untuk mengajukan izin konsinyering?<br>-	Surat Permohonan Izin yang telah ditandatangani oleh penanggung jawab kegiatan (Pejabat Eselon 2)
<br>-	Kerangka Acuan Kerja (KAK) yang menjelaskan mengenai urgensi dan tingkat pentingnya kegiatan
<br>-	Formulir informasi Manajemen Risiko (MR) dalam format pdf
<br>-	Pengajuan surat izin konsinyering yang disampaikan kurang dari 3 hari kerja sebelum pelaksanaan kegiatan, maka wajib melampirkan surat undangan konsinyering dan kesediaan hadir narasumber (apabila menghadirkan narasumber)
</li>
<li>Apa saja yang perlu diperhatikan dalam mengajukan izin konsinyering?
<br>-	Dalam pengisian MR
<br> a. Pastikan sasaran dan indikator sesuai dengan PK Eselon 2 terbaru
<br> b. Pastikan indikator output kegiatan sesuai dengan RKAKL terbaru
<br> c. Pastikan anggaran yang digunakan sesuai dengan kegiatan yang dilaksanakan, dan mencantumkan anggaran yang digunakan secara lengkap (sampai dengan kode akun)
<br>
-	 Apabila pengajuan izin konsinyering kurang dari 3 (tiga) hari kerja maka diwajibkan melampirkan undangan konsinyering dan undangan narasumber (apabila ada)</li>
</ol>
      <h2>
      Gratifikasi
      </h2>
      <ol>
<li>Apa dasar hukum peraturan gratifikasi? <br>
-	Undang-Undang Nomor 20 Tahun 2001 tentang Perubahan atas UU Nomor 31 Tahun 1999 tentang Tindak Pidana Korupsi (di menu download)
</li>
-	Surat KPK b-1341 tentang Pedoman dan Batasan Gratifikasi (di menu download)
<br>
-	Peraturan Menteri Pendayagunaan Aparatur Negara dan Reformasi Birokrasi Nomor 4 Tahun 2015 tentang Pengendalian Gratifikasi di Lingkungan Kementerian PANRB (di menu download)
</li>
<li>
Apa yang dimaksud dengan Gratifikasi? <br>
-	Berdasarkan Undang-Undang Nomor 20 Tahun 2001 tentang Tindak Pidana Korupsi, gratifikasi adalah pemberian uang, barang, rabat (discount), komisi, pinjaman tanpa bunga, tiket perjalanan, fasilitas penginapan, perjalanan wisata, pengobatan cuma-cuma dan fasilitas lainnya. Gratifikasi tersebut baik yang diterima di dalam negeri maupun di luar negeri dan yang dilakukan dengan menggunakan sarana elektronik atau tanpa sarana elektronik. (di menu download)
</li>
<li>Bagaimana apabila tidak dapat menolak gratifikasi?
<br>-	Apabila Anda tidak dapat menolak gratifikasi karena kondisi sebagai berikut:
<br> a.	Gratifikasi tidak diterima secara langsung
<br> b.	Tidak diketahuinya pemberi gratifikasi
<br> c.	Penerima ragu dengan kualifikasi gratifikasi
<br> d.	Adanya kondisi tertentu yang tidak mungkin ditolak, seperti: dapat mengakibatkan rusaknya hubungan baik institusi, membahayakan diri sendiri/karir penerima/ada ancaman lain.
<br> Maka untuk menghindari ancaman pidana, Pegawai Negeri/Penyelenggara Negara wajib melaporkan kepada KPK paling lambat 30 (tiga puluh) hari sejak tanggal penerimaan gratifikasi tersebut atau melalui Unit Pengendalian Gratifikasi Instansi paling lambat 7 (tujuh) hari kerja yang kemudian diteruskan ke KPK.
<li>	Penerimaan gratifikasi apa saja yang dikecualikan untuk wajib dilaporkan? <br>
-	Dapat dilihat pada Surat KPK b-1341 tentang Pedoman dan Batasan Gratifikasi (di menu download)</li>
<li>Bagaimana perlakuan pada gratifikasi yang berupa barang mudah busuk atau rusak dalam batasan kewajaran?<br>
-	Barang tersebut dapat disalurkan langsung ke panti asuhan, panti jompo, pihak-pihak yang membutuhkan atau tempat penyaluran bantuan social lainnya dan dilaporkan kepada masing-masing instansi disertai penjelasan taksiran harga dan dokumentasi penyerahannya. Selanjutnya instansi melaporkan rekapitulasi penerimaan tesebut kepada KPK.
</li>
<li>Bagaimana perlakuan untuk barang gratifikasi yang direkomendasikan untuk dikelola instansi?
<br>-	Ditempatkan sebagai barang display instansi
<br>-	Digunakan untuk kegiatan operasional instansi
<br>-	Disalurkan kepada pihak yang membutuhkan antara lain, panti asuhan, panti jompo, atau tempat penyaluran bantuan social lainnya, atau
<br>-	Diserahkan kepada pegawai yang menerima gratifikasi untuk dimanfaatkann sebagai penunjang kinerja.
</li>
<li>Bagaimana cara melaporkan gratifikasi?
<br>-	Dapat melaporkan ke UPG-UPG pada unit kerja masing-masing, dan dapat dilihat melaui skema pelaporan gratifikasi (di menu download)
</li>
<li>Berapa jangka waktu maksimal pelaporan? Dan bagaimana bila melebihi jangka waktu tersebut?
<br>-	Paling lambat dapat dilaporkan 15 (lima belas) hari kalender sejak diterimanya, ditolaknya, maupun diberikannya gratifikasi oleh penyelenggara negara dan pegawai yang bersangkutan.
</li>

